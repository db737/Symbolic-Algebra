package function;

/* Allowed types of functions of one variable.
 */
public enum T1 {
	
	NEG,		// negation ie -x
	SQ, 		// square function ie x * x
	CU, 		// cube function ie x * x * x
	INV,		// inverse ie 1/x
	SQRT,		// square root
	INVSQRT,	// inverse square root ie 1/sqrt(x)
	EXP,		// exponential function
	LN,			// natural logarithm
	SIN,		// sine
	COS,		// cosine
	TAN,		// tangent
	ASIN,		// inverse sine
	ACOS,		// inverse cosine
	ATAN,		// inverse tangent
	COSEC,		// cosecant
	SEC,		// secant
	COT,		// cotangent
	SINH,		// hyperbolic sine
	COSH,		// hyperbolic cosine
	TANH,		// hyperbolic tangent
	COSECH,		// hyperbolic cosecant
	SECH,		// hyperbolic secant
	COTH		// hyperbolic cotangent
	
}